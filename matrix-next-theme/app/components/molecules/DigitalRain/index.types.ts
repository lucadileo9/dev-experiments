export default interface DigitalRainProps {
    fontSize?: number; // Dimensione dei caratteri (default: 14)
    color?: string; // Colore dei caratteri (default: "#00FF41")
    speed?: number; // Velocità dell'animazione in millisecondi (default: 35)
    characters?: string; // Stringa contenente i caratteri visualizzabili (default: alfabeto + numeri)
    backgroundOpacity?: number; // Opacità dello sfondo (default: 0.05)
 }